<h1>Apoie as Festas Lulinas</h1>

<p class="lead">
    As Festas Lulinas são um movimento popular, feito por e para o povo brasileiro. Não contamos com o apoio de grandes empresas, partidos ou políticos influentes. Nosso financiamento vem da força e da solidariedade daqueles que acreditam na cultura popular e na construção de um Brasil mais justo e alegre.
</p>

<h2>Por que Apoiar?</h2>

<p>
    As Festas Lulinas são mais do que simples festas. São um movimento de resistência cultural, de celebração da nossa identidade e de luta por um futuro melhor. Ao apoiar as Festas Lulinas, você contribui para:
</p>

<ul>
    <li><strong>Manter viva a tradição das festas juninas e julinas:</strong> Uma das mais ricas e autênticas manifestações da cultura popular brasileira.</li>
    <li><strong>Promover a união e a solidariedade:</strong> As festas são momentos de encontro, partilha e fortalecimento dos laços comunitários.</li>
    <li><strong>Combater a fome e a miséria:</strong> Reafirmamos nosso compromisso com a justiça social e a construção de um país onde todos tenham acesso a uma vida digna.</li>
    <li><strong>Fortalecer a cultura popular:</strong> Incentivamos a produção artística e cultural independente, valorizando as raízes do nosso povo.</li>
    <li><strong>Homenagear o legado de Lula:</strong> Celebramos as conquistas e os valores defendidos pelo presidente Lula, que tanto contribuiu para a melhoria da vida dos brasileiros.</li>
</ul>

<h2>Nossa Meta</h2>

<p>
    Para realizar as Festas Lulinas em todo o Brasil, precisamos arrecadar <strong>R$ 35.000,00</strong> por ano. Esse valor será usado para:
</p>

<ul>
    <li>Produzir e distribuir gratuitamente materiais de divulgação, como cartazes, banners e panfletos.</li>
    <li>Adquirir e distribuir adereços e brindes temáticos para os participantes das festas.</li>
    <li>Custear o deslocamento e a hospedagem dos nossos organizadores e colaboradores, que viajam pelo país para apoiar a realização das festas em diferentes comunidades.</li>
    <li>Apoiar a infraestrutura básica das festas, como som, iluminação e decoração.</li>
</ul>

